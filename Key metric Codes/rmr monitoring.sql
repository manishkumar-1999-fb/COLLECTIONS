-------------------------------------------------------------------TABLE 3
--------------------------------USE OF THE TABLE IS: TAGGING THE OLDEST RMR MARGINAL ENTER DATE AND RECENT RMR DATE------------------------------------------------
-----------------------------------------CREATING TABLE UNIQUE ON FBBID X EDATE LEVEL WHICH CAPTURES THE RMR MARGINAL ENTER AND RMR DATE-------------------------------------
CREATE OR REPLACE TABLE ANALYTICS.CREDIT.RMR_MARGINAL_SEGMENT_TIME AS
SELECT *
, MIN(MARGINAL_POP_SEGMENT_DATE)OVER(PARTITION BY FBBID ORDER BY EDATE ASC) AS MARGINAL_POP_SEGMENT_START_DATE
, MAX(RMR_POP_SEGMENT_DATE)OVER(PARTITION BY FBBID ORDER BY EDATE ASC) AS RMR_POP_SEGMENT_START_DATE
, MIN(SUSPENDED_ACCOUNT_DATE)OVER(PARTITION BY FBBID ORDER BY EDATE ASC) AS ACCOUNT_SUSPENDED_START_DATE
, CASE
    WHEN RMR_POP_SEGMENT_START_DATE = CURRENT_DATE() AND DAYOFWEEK(RMR_POP_SEGMENT_START_DATE) <> 3 THEN NULL
    WHEN DATEDIFF(DAY, RMR_POP_SEGMENT_START_DATE, DATE_TRUNC(WEEK, CURRENT_DATE()+4)::DATE - 5) < 0 THEN CURRENT_DATE()-1
    ELSE DATE_TRUNC(WEEK, RMR_POP_SEGMENT_START_DATE::DATE + 4)::DATE+2
END AS RMR_POP_SEGMENT_START_WEEK_END
, CASE
    WHEN MARGINAL_POP_SEGMENT_START_DATE = CURRENT_DATE() AND DAYOFWEEK(MARGINAL_POP_SEGMENT_START_DATE) <> 3 THEN NULL
    WHEN DATEDIFF(DAY, MARGINAL_POP_SEGMENT_START_DATE, DATE_TRUNC(WEEK, CURRENT_DATE()+4)::DATE - 5) < 0 THEN CURRENT_DATE()-1
    ELSE DATE_TRUNC(WEEK, MARGINAL_POP_SEGMENT_START_DATE::DATE + 4)::DATE+2
END AS MARGINAL_POP_SEGMENT_START_WEEK_END
, CASE
    WHEN IS_RMR = 1 OR DATEDIFF(DAY, RMR_POP_SEGMENT_START_DATE, WEEK_END) BETWEEN 0 AND 90 THEN 'RMR SEGMENT'
    WHEN MARGINAL_POP_SEGMENT_START_DATE <= '2024-05-29' THEN 'NO RMR SEGMENT'
    WHEN DATEDIFF(DAY, MARGINAL_POP_SEGMENT_START_DATE, WEEK_END) BETWEEN 0 AND 90 AND IS_SUSPENDED_ACCOUNT = 0 THEN 'MARGINAL SEGMENT'
    ELSE 'NO RMR SEGMENT'
END AS FINAL_SEGMENT
, ROW_NUMBER()OVER(PARTITION BY FBBID, EDATE ORDER BY LOCK_START_LTZ DESC) AS DACD_ROW_NUM
FROM ANALYTICS.CREDIT.RMR_MONITORING_LABELING_TABLE
QUALIFY DACD_ROW_NUM = 1;

