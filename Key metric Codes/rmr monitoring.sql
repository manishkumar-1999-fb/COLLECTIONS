---------DPD 1->2--------
, SUM(CASE WHEN DPD_PREV_WEEK BETWEEN 1 AND 7 AND DPD_DAYS_CORRECTED BETWEEN 8 AND 14 THEN OUTSTANDING_PRINCIPAL_DUE END) AS TOTAL_OUTSTANDING_BUCKET_2_THIS_WEEK
, SUM(CASE WHEN DPD_PREV_WEEK BETWEEN 1 AND 7 THEN OUSTANDING_PREV_WEEK END) AS TOTAL_OUTSTANDING_BUCKET_1_LAST_WEEK
, (TOTAL_OUTSTANDING_BUCKET_2_THIS_WEEK/TOTAL_OUTSTANDING_BUCKET_1_LAST_WEEK) AS PERCENTAGE_DOLLAR_ROLL_RATE_BUCKET_1_2
---------DPD 2->3--------
, SUM(CASE WHEN DPD_PREV_WEEK BETWEEN 8 AND 14 AND DPD_DAYS_CORRECTED BETWEEN 15 AND 35 THEN OUTSTANDING_PRINCIPAL_DUE END) AS TOTAL_OUTSTANDING_BUCKET_3_THIS_WEEK
, SUM(CASE WHEN DPD_PREV_WEEK BETWEEN 8 AND 14 THEN OUSTANDING_PREV_WEEK END) AS TOTAL_OUTSTANDING_BUCKET_2_LAST_WEEK
, (TOTAL_OUTSTANDING_BUCKET_3_THIS_WEEK/TOTAL_OUTSTANDING_BUCKET_2_LAST_WEEK) AS PERCENTAGE_DOLLAR_ROLL_RATE_BUCKET_2_3
---------DPD 3->4--------
, SUM(CASE WHEN DPD_PREV_WEEK BETWEEN 15 AND 35 AND DPD_DAYS_CORRECTED BETWEEN 36 AND 63 THEN OUTSTANDING_PRINCIPAL_DUE END) AS TOTAL_OUTSTANDING_BUCKET_4_THIS_WEEK
, SUM(CASE WHEN DPD_PREV_WEEK BETWEEN 15 AND 35 THEN OUSTANDING_PREV_WEEK END) AS TOTAL_OUTSTANDING_BUCKET_3_LAST_WEEK
, (TOTAL_OUTSTANDING_BUCKET_4_THIS_WEEK/TOTAL_OUTSTANDING_BUCKET_3_LAST_WEEK) AS PERCENTAGE_DOLLAR_ROLL_RATE_BUCKET_3_4
---------DPD 4->5--------
, SUM(CASE WHEN DPD_PREV_WEEK BETWEEN 36 AND 63 AND DPD_DAYS_CORRECTED BETWEEN 64 AND 97 THEN OUTSTANDING_PRINCIPAL_DUE END) AS TOTAL_OUTSTANDING_BUCKET_5_THIS_WEEK
, SUM(CASE WHEN DPD_PREV_WEEK BETWEEN 36 AND 63 THEN OUSTANDING_PREV_WEEK END) AS TOTAL_OUTSTANDING_BUCKET_4_LAST_WEEK
, (TOTAL_OUTSTANDING_BUCKET_5_THIS_WEEK/TOTAL_OUTSTANDING_BUCKET_4_LAST_WEEK) AS PERCENTAGE_DOLLAR_ROLL_RATE_BUCKET_4_5
---------DPD 5->6--------
, SUM(CASE WHEN DPD_PREV_WEEK BETWEEN 64 AND 97 AND DPD_DAYS_CORRECTED > 97 THEN OUTSTANDING_PRINCIPAL_DUE END) AS TOTAL_OUTSTANDING_CO_THIS_WEEK
, SUM(CASE WHEN DPD_PREV_WEEK BETWEEN 64 AND 97 THEN OUSTANDING_PREV_WEEK END) AS TOTAL_OUTSTANDING_BUCKET_5_LAST_WEEK
, (TOTAL_OUTSTANDING_CO_THIS_WEEK/TOTAL_OUTSTANDING_BUCKET_5_LAST_WEEK) AS PERCENTAGE_DOLLAR_ROLL_RATE_BUCKET_5_CO
FROM ( SELECT *
, LAG(DPD_DAYS_CORRECTED)OVER(PARTITION BY LOAN_KEY ORDER BY FMD_WEEK_END ASC) AS DPD_PREV_WEEK
, LAG(OUTSTANDING_PRINCIPAL_DUE)OVER(PARTITION BY LOAN_KEY ORDER BY FMD_WEEK_END ASC) AS OUSTANDING_PREV_WEEK
FROM MARGINAL_LOANS_FMD_DATA
) AS SUB_QUERY
GROUP BY 1
)
SELECT A1.WEEK_END AS WEEK_END_DATE
, A1.NUMBER_OF_MARGINAL_CUSTOMERS
, A1.NUMBER_OF_RMR_CUSTOMERS
, A1.NUMBER_OF_SUSPENDED_CUSTOMERS
, SUM(A4.MARGINAL_ORIGINATIONS_THIS_WEEK)OVER(ORDER BY A1.WEEK_END ASC ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS CUMULATIVE_ORIGINATIONS
, A2.NUM_MARGINAL_ENTER_ACCOUNTS_THIS_WEEK
, COALESCE(A3.NUM_RMR_ENTER_ACCOUNTS_THIS_WEEK,0) AS NUM_RMR_ENTER_ACCOUNTS_THIS_WEEK
, A4.MARGINAL_ORIGINATIONS_THIS_WEEK
, A4.MARGINAL_DRAWS_THIS_WEEK
--------TOTAL OUTSTANDING FROM THE LOANS
, A5.TOTAL_OUTSTANDING
--------SPLIT BY TENURES
, A5.TOTAL_OS_TENURE_5
, A5.TOTAL_OS_TENURE_3_5
, A5.TOTAL_OS_TENURE_1_3
, A5.TOTAL_OS_TENURE_12MON
, A5.TOTAL_OS_TENURE_6MON
--------TOTAL DPD OUSTANDING FROM THESE LOANS
, A5.TOTAL_DPD_OUTSTANDING
--------SPLIT BY TENURE
, A5.TOTAL_DPD_OS_TENURE_5
, A5.TOTAL_DPD_OS_TENURE_3_5
, A5.TOTAL_DPD_OS_TENURE_1_3
, A5.TOTAL_DPD_OS_TENURE_12MON
, A5.TOTAL_DPD_OS_TENURE_6MON
----------PERCENTAGE DPD OUSTANDING--------------------
, TOTAL_DPD_OUTSTANDING/TOTAL_OUTSTANDING AS PERCENTAGE_DPD_OUTSTANDING
----------SPLIT BY TENURE
, CASE WHEN TOTAL_OS_TENURE_5 <> 0 THEN TOTAL_DPD_OS_TENURE_5/TOTAL_OS_TENURE_5 END AS PERCENTAGE_DPD_OUTSTANDING_TENURE_5
, CASE WHEN TOTAL_OS_TENURE_3_5 <> 0 THEN TOTAL_DPD_OS_TENURE_3_5/TOTAL_OS_TENURE_3_5 END AS PERCENTAGE_DPD_OUTSTANDING_TENURE_3_5
, CASE WHEN TOTAL_OS_TENURE_1_3 <> 0 THEN TOTAL_DPD_OS_TENURE_1_3/TOTAL_OS_TENURE_1_3 END AS PERCENTAGE_DPD_OUTSTANDING_TENURE_1_3
, CASE WHEN TOTAL_OS_TENURE_12MON <> 0 THEN TOTAL_DPD_OS_TENURE_12MON/TOTAL_OS_TENURE_12MON END AS PERCENTAGE_DPD_OUTSTANDING_TENURE_12MON
, CASE WHEN TOTAL_OS_TENURE_6MON <> 0 THEN TOTAL_DPD_OS_TENURE_6MON/TOTAL_OS_TENURE_6MON END AS PERCENTAGE_DPD_OUTSTANDING_TENURE_6MON
----------SPLIT BY DPD BUCKETS
, A5.TOTAL_DPD_OUSTANDING_BUCKET_1
, A5.TOTAL_DPD_OUSTANDING_BUCKET_2
, A5.TOTAL_DPD_OUSTANDING_BUCKET_3
, A5.TOTAL_DPD_OUSTANDING_BUCKET_4
, A5.TOTAL_DPD_OUSTANDING_BUCKET_5
, A5.NUMBER_OF_DPD_CUSTOMERS
, A5.NUMBER_DPD_OUSTANDING_BUCKET_1
, A5.NUMBER_DPD_OUSTANDING_BUCKET_2
, A5.NUMBER_DPD_OUSTANDING_BUCKET_3
, A5.NUMBER_DPD_OUSTANDING_BUCKET_4
, A5.NUMBER_DPD_OUSTANDING_BUCKET_5
, A5.TOTAL_MARGINAL_CHARGEOFF
, A6.TOTAL_OUTSTANDING_BUCKET_2_THIS_WEEK
-------------------ROLL RATES--------------
, ROUND(COALESCE(A6.PERCENTAGE_DOLLAR_ROLL_RATE,0),4) AS PERCENTAGE_DOLLAR_ROLL_RATE
-------------------
, ROUND(COALESCE(A6.PERCENTAGE_DOLLAR_ROLL_RATE_BUCKET_1_2,0),4) AS PERCENTAGE_DOLLAR_ROLL_RATE_1_2
, ROUND(COALESCE(A6.PERCENTAGE_DOLLAR_ROLL_RATE_BUCKET_2_3,0),4) AS PERCENTAGE_DOLLAR_ROLL_RATE_2_3
, ROUND(COALESCE(A6.PERCENTAGE_DOLLAR_ROLL_RATE_BUCKET_3_4,0),4) AS PERCENTAGE_DOLLAR_ROLL_RATE_3_4
, ROUND(COALESCE(A6.PERCENTAGE_DOLLAR_ROLL_RATE_BUCKET_4_5,0),4) AS PERCENTAGE_DOLLAR_ROLL_RATE_4_5
, ROUND(COALESCE(A6.PERCENTAGE_DOLLAR_ROLL_RATE_BUCKET_5_CO,0),4) AS PERCENTAGE_DOLLAR_ROLL_RATE_5_CO
, A6.TOTAL_OUTSTANDING_DELINQUENT_THIS_WEEK AS OS_DPD_1_7_THIS_WEEK
, A6.TOTAL_OUTSTANDING_NON_DELINQUENT_PREV_WEEK AS OS_DPD_0_PREV_WEEK
FROM STOCK_VIEW AS A1
LEFT JOIN MARGINAL_ENTER_CUSTOMERS AS A2 ON A1.WEEK_END = A2.MARGINAL_POP_SEGMENT_START_WEEK_END
LEFT JOIN RMR_ENTER_CUSTOMERS AS A3 ON A1.WEEK_END = A3.RMR_POP_SEGMENT_START_WEEK_END
LEFT JOIN MARGINAL_ORIGINATIONS AS A4 ON A1.WEEK_END = A4.WEEK_END
LEFT JOIN AGGREGATED_DPD_METRICS AS A5 ON A1.WEEK_END = A5.FMD_WEEK_END
LEFT JOIN ROLL_RATE AS A6 ON A1.WEEK_END = A6.FMD_WEEK_END
WHERE A1.WEEK_END >= '2024-06-05' AND A1.WEEK_END <= CURRENT_DATE;

