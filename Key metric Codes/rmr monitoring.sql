------------------------------------PURPOSE OF THE TABLE: CREATING A MODIFIED DAILY APPROVED CUSTOMERS DATA WITH CALCULATED FIELDS------------------------------------------
------------------------------------------------------------------------------------------THE BELOW TABLE WILL BE USED WHENEVER DAILY APPROVED CUSTOMERS DATA IS REQUIRED------------------------------------------------------------------------
CREATE OR REPLACE TABLE ANALYTICS.CREDIT.DAILY_APPROVED_CUSTOMERS_DATA_UPDATED AS
SELECT A1.*
, DATE_TRUNC('WEEK', A1.EDATE::DATE+4)::DATE-4 AS WEEK_START
, CASE
    WHEN A1.EDATE = CURRENT_DATE() AND DAYOFWEEK(A1.EDATE) <> 3 THEN NULL
    WHEN DATEDIFF(DAY, A1.EDATE, DATE_TRUNC('WEEK',CURRENT_DATE()+4)::DATE-5) < 0 THEN CURRENT_DATE()-1
    ELSE DATE_TRUNC('WEEK', A1.EDATE::DATE+4)::DATE + 2
END AS WEEK_END
, CREDIT_LIMIT - OUTSTANDING_PRINCIPAL AS OPEN_TO_BUY
, CREDIT_SCORE_JSON:"VantageScore 4.0"."score" AS VANTAGE4
, A2.CHANNEL
, A2.PARTNER
, A2.NEW_CUST_FILTER
, A2.OB_BUCKET_GROUP_DAL
, A2.OG_BUCKET_GROUP
, CASE 
	WHEN DPD_DAYS IS NULL AND A1.IS_CHARGEOFF = 0 THEN 0
	WHEN DPD_DAYS IS NULL AND A1.IS_CHARGEOFF = 1 THEN 98
	ELSE DPD_DAYS
END AS DPD_DAYS_CORRECTED
, CASE 
	WHEN DPD_DAYS_CORRECTED BETWEEN 1 AND 7 THEN 'DPD 1-7' 
	WHEN DPD_DAYS_CORRECTED BETWEEN 8 AND 14 THEN 'DPD 8-14'
	WHEN DPD_DAYS_CORRECTED BETWEEN 15 AND 35 THEN 'DPD 15-35'
	WHEN DPD_DAYS_CORRECTED BETWEEN 36 AND 63 THEN 'DPD 36-63'
	WHEN DPD_DAYS_CORRECTED BETWEEN 64 AND 97 THEN 'DPD 64-90'
END DPD_GROUP
, DATEDIFF(MONTH, A1.FIRST_APPROVED_TIME, A1.EDATE) AS AGE_OF_ACCOUNT
, GREATEST(IFNULL(FIRST_ACCOUNT_SIZE_ACCOUNTING_SOFTWARE,0), IFNULL(FIRST_ACCOUNT_SIZE_FI,0)) * 12 ANNUAL_REVENUE
, MAX(DPD_DAYS_CORRECTED) OVER (PARTITION BY A1.FBBID ORDER BY A1.EDATE DESC ROWS BETWEEN 30 PRECEDING AND CURRENT ROW) AS DPD_LAST30DAYS
FROM BI.PUBLIC.DAILY_APPROVED_CUSTOMERS_DATA AS A1
LEFT JOIN INDUS.PUBLIC.INDUS_KEY_METRICS_FILTERS AS A2 ON A1.FBBID = A2.FBBID AND A1.EDATE = A2.EDATE
LEFT JOIN BI.PUBLIC.CUSTOMERS_DATA AS A3 ON A1.FBBID = A3.FBBID
WHERE A1.EDATE >= '2024-04-15' AND UPPER(A1.SUB_PRODUCT) = 'LINE OF CREDIT' AND IS_TEST_USER = 0;

