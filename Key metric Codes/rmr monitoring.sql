------------------------------------PURPOSE OF THE TABLE: CREATING A MODIFIED FMD TABLE WITH THE EXCUSION OF CANCELLED LOANS, ADDITION OF CALCULATED FIELD, SINCE THESE LOANS ARE NOT REQUIRED TO BE MONITORED------------------------------------------
------------------------------------------------------------------------------------------THE BELOW TABLE WILL BE USED WHENEVER FINANCE METRICS DAILY IS REQUIRED---------------------------------------------------------------------------------------

CREATE OR REPLACE TABLE ANALYTICS.CREDIT.FINANCE_METRICS_DAILY_UPDATED AS
SELECT A1.*
, TO_DATE(LOAN_CREDITED_DATE::STRING) as LCD
, CASE
    WHEN LOAN_CREATED_DATE = CURRENT_DATE() AND DAYOFWEEK(LOAN_CREATED_DATE) <> 3 THEN NULL
    WHEN DATEDIFF(DAY, LOAN_CREATED_DATE, DATE_TRUNC('WEEK',CURRENT_DATE()+4)::DATE-5) < 0 THEN CURRENT_DATE()-1
    ELSE DATE_TRUNC('WEEK', LOAN_CREATED_DATE::DATE+4)::DATE + 2
END AS LOAN_CREATED_WEEK_END
, CASE
    WHEN EDATE = CURRENT_DATE() AND DAYOFWEEK(EDATE) <> 3 THEN NULL
    WHEN DATEDIFF(DAY, EDATE, DATE_TRUNC('WEEK',CURRENT_DATE()+4)::DATE-5) < 0 THEN CURRENT_DATE()-1
    ELSE DATE_TRUNC('WEEK', EDATE::DATE+4)::DATE + 2
END AS FMD_WEEK_END
, CASE 
	WHEN DPD_DAYS IS NULL AND IS_CHARGED_OFF = 0 THEN 0
	WHEN DPD_DAYS IS NULL AND IS_CHARGED_OFF = 1 THEN 98
	ELSE DPD_DAYS
END AS DPD_DAYS_CORRECTED
, MAX(EDATE)OVER(PARTITION BY A1.FBBID, LOAN_KEY ORDER BY EDATE DESC) AS MAX_EDATE
FROM (
SELECT * FROM BI.FINANCE.FINANCE_METRICS_DAILY 
WHERE PRODUCT_TYPE <> 'Flexpay' 
AND (loan_operational_status <> 'CNCL' OR loan_operational_status IS NULL) AND LOAN_CREATED_DATE >= '2024-01-01') AS A1
;

