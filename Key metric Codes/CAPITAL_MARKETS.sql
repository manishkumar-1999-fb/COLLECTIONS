SET RISKLIMIT = 0.04;
SET WA_RISK_SCORE = 4.5;
SET TERM_GT_TWENTY_FOUR = 0.2;
SET TERM_GT_TWENTY_FOUR_RISK_RANGE = 0.04;
SET ModifiedReceivablesLimit_CS = 0.05;
SET ModifiedReceivablesLimit_CRB = 0.025;
SET VantageScoreLimit = 0.1;
SET Limit_100K = 0.05;
SET Limit_120K = 0.025;
SET Credit_Limit_Average = 30000;

-- Advance Rate
SET CRBWAM_Advance_Rate = 0.95;
SET CS_Advance_Rate = 0.91;

--Raw Data used for calculations
CREATE OR REPLACE TEMPORARY TABLE FACILITY_BASE2 AS
WITH pledging_dates as (
select edate, lag(edate) over(order by edate asc) as previous_pledge_date from ANALYTICS.CAPITAL_MARKETS.PLEDGE_DATES 
),
BREATHERS AS
(
	SELECT
	    FBBID,
	    FROM_DATE BREATHER_START_DATE,
	    TO_DATE BREATHER_END_DATE,
	    ROUND((EVENT_JSON:breather_days)/7,0) BREATHER_PLAN_COUNT
	FROM BI.FINANCE.DIM_BREATHER_EVENT
	WHERE FROM_DATE IS NOT NULL
),
Repurchased_loan_date AS
(SELECT  LOAN_KEY,
CASE WHEN Set_SPV.FROM_SPV IN ( 'FBX_PT6',  'FBX_PT5','FBX_PT4') AND Set_SPV.TO_SPV =  'FBX_INC' THEN Set_SPV.Edate ELSE NULL end AS "Repurchase Date",
Set_SPV.FROM_SPV,
FROM SETPOINT_APPROVED_REPORT_DATA.RESULTS.REPORT_APPROVAL_RESULTS AS Set_SPV),
MODIFICATIONS AS
(
	SELECT
	    A.LOAN_KEY,
        EDATE,
--        rolling sum
    	SUM(CASE WHEN ACTIVE_PAYMENT_PLAN_NAME IN ('AMOUNT_BASED_QUAD_WEEKLY', 'AMOUNT_BASED_WEEKLY') AND A.EDATE = ACTIVE_PAYMENT_PLAN_START_DATE THEN 1 ELSE 0 END) OVER(PARTITION BY A.LOAN_KEY ORDER BY A.EDATE ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) CUSTOM_PLAN_COUNT
	    --SUM(DISTINCT CASE WHEN LOAN_OPERATIONAL_STATUS = 'FROZ' THEN 1 ELSE 0 END) OVER(PARTITION BY A.LOAN_KEY ORDER BY A.EDATE ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) WAIVER_COUNT
--		Least(SUM( CASE WHEN LOAN_OPERATIONAL_STATUS = 'FROZ' THEN 1 ELSE 0 END) OVER(PARTITION BY A.LOAN_KEY ORDER BY A.EDATE ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW),1) WAIVER_COUNT
    	FROM BI.FINANCE.FINANCE_METRICS_DAILY A
),
FBXPLUS AS 
(
    SELECT 
            DACD.FBBID,
            DACD.EDATE,
            1 FBX_PLUS
    FROM BI."PUBLIC".DAILY_APPROVED_CUSTOMERS_DATA DACD
    WHERE UPPER(DACD.FUNDBOX_PLUS_STATUS) LIKE '%SUBSCRIBED%'
    	AND YEAR(EDATE) > 2023
    GROUP BY ALL
),
BK_FRAUD AS
(
	SELECT FBBID,
			EDATE,
			IS_FRAUD,
			BK_FILING_DATE
	FROM BI.PUBLIC.DAILY_APPROVED_CUSTOMERS_DATA
	WHERE YEAR(EDATE) > 2023
	GROUP BY ALL
),
ACQ_CHANNEL_CALC AS
(
	SELECT
		A.FBBID,
		A.LOAN_KEY,
		CASE
			WHEN LOWER(REGEXP_REPLACE(TRIM(CD.REGISTRATION_CAMPAIGN_ID), '\s+\d+$', '')) = LOWER(REGEXP_REPLACE(TRIM(TB.TERMINATED_BROKERS), '\s+\d+$', '')) THEN 'Brokers'
			WHEN PN.PARTNER_NAME = 'Intuit' THEN 'Intuit'
			WHEN LOWER(CD.REGISTRATION_CAMPAIGN_ID) IN ('intuit', 'intuit_appcenter', 'quickbooks-financing', 'intuit_campaigns') OR LOWER(REGISTRATION_CAMPAIGN_ID) LIKE '%intuit%' THEN 'Intuit'
			WHEN PN.PARTNER_NAME = 'Nav' THEN 'NAV'
			WHEN PN.PARTNER_NAME IN ('Lendingtree', 'Lendio') THEN 'Marketplace'
			WHEN LOWER(REGEXP_REPLACE(TRIM(CD.PARTNER_ATTRIBUTION), '\s+\d{4}$', '')) = LOWER(REGEXP_REPLACE(TRIM(PN.PARTNER_ATTRIBUTION), '\s+\d{4}$', '')) THEN 'Strategic Partners'
			WHEN LOWER(REGEXP_REPLACE(TRIM(CD.PARTNER_ATTRIBUTION), '\s+\d{4}$', '')) = LOWER(REGEXP_REPLACE(TRIM(PN.PARTNER_NAME), '\s+\d{4}$', '')) THEN 'Strategic Partners'
			WHEN LOWER(CD.PARTNER_ATTRIBUTION) IN ('organic','content', 'fundboxpay', 'content 100817571549') THEN 'Organic'
	        WHEN LOWER(CD.PARTNER_ATTRIBUTION) LIKE '%direct%' THEN 'Direct'
			WHEN (PN.PARTNER_NAME IS NULL AND CD.PARTNER_ATTRIBUTION IN ('non-partner')) THEN 'Organic'
			WHEN (PN.PARTNER_NAME IS NULL) THEN 'Terminated Channel'
			ELSE 'Brokers'
		END ACQ_CHANNEL
	FROM BI.FINANCE.FINANCE_METRICS_DAILY A
	LEFT JOIN BI."PUBLIC".CUSTOMERS_DATA cd 
	ON CD.FBBID = A.FBBID
	LEFT JOIN ANALYTICS.CREDIT.EG_PA_TO_PN PN 
		ON LOWER(REGEXP_REPLACE(TRIM(CD.PARTNER_ATTRIBUTION), '\s+\d{4}$', '')) = LOWER(REGEXP_REPLACE(TRIM(PN.PARTNER_ATTRIBUTION), '\s+\d{4}$', ''))
			OR LOWER(REGEXP_REPLACE(TRIM(CD.PARTNER_ATTRIBUTION), '\s+\d{4}$', '')) = LOWER(REGEXP_REPLACE(TRIM(PN.PARTNER_NAME), '\s+\d{4}$', ''))
	LEFT JOIN ANALYTICS.CAPITAL_MARKETS.TERMINATED_BROKERS TB
		ON LOWER(REGEXP_REPLACE(TRIM(CD.REGISTRATION_CAMPAIGN_ID), '\s+\d{4}$', '')) = LOWER(REGEXP_REPLACE(TRIM(TB.TERMINATED_BROKERS), '\s+\d{4}$', ''))
	GROUP BY ALL
),
OG_CDC AS (
  SELECT
    D.FBBID,
    D.EDATE,
    LAST_VALUE(CASE WHEN D.RISK_CREDIT_REVIEW_MODEL_NAME IN ('UnifiedOngoingMLEModelV2.2', 'sm-internalmodel_unified_og_v2.2_shap', 'smm-ongoing-SV-v2-2-shap', 'smm-ongoing-SV-v2-2-shap-sbd-v2') 
    	THEN D.RISK_CREDIT_REVIEW_ALIGNED_BUCKET ELSE OG.RETRO_SCORE END) OVER(PARTITION BY D.FBBID, D.EDATE ORDER BY D.EDATE) AS OG_SCORE
  FROM BI.PUBLIC.DAILY_APPROVED_CUSTOMERS_DATA AS d
    LEFT JOIN (
      SELECT
        FBBID,
        SCORE_DT,
        FIRST_VALUE(UNI_BKT) IGNORE NULLS OVER(PARTITION BY FBBID ORDER BY SCORE_DT) AS RETRO_SCORE,
        FIRST_VALUE(OG_SCORE) IGNORE NULLS OVER(PARTITION BY FBBID ORDER BY SCORE_DT) AS SCORE_SPECIFIC
      FROM
        (
          SELECT
            CAST(PREDICTION_TIME AS DATE) AS SCORE_DT,
            IDENTIFIER_ID AS FBBID,
            SCORE AS OG_SCORE,
            CASE
              WHEN SCORE <= 0.016 THEN 1
              WHEN SCORE <= 0.019 THEN 2
              WHEN SCORE <= 0.025 THEN 3
              WHEN SCORE <= 0.03 THEN 4
              WHEN SCORE <= 0.036 THEN 5
              WHEN SCORE <= 0.043 THEN 6
              WHEN SCORE <= 0.052 THEN 7
              WHEN SCORE <= 0.063 THEN 8
              WHEN SCORE <= 0.08 THEN 9
              WHEN SCORE <= 0.11 THEN 10
              WHEN SCORE <= 0.122 THEN 11
              WHEN SCORE <= 0.135 THEN 12
              WHEN SCORE <= 0.156 THEN 13
              WHEN SCORE <= 0.195 THEN 14
              WHEN SCORE <= 1 THEN 15
            END AS UNI_BKT
          FROM CDC_V2.SCORING.SCORING_MODEL_SCORES
          WHERE MODEL_NAME IN ('UnifiedOngoingMLEModelV2.2', 'sm-internalmodel_unified_og_v2.2_shap', 'smm-ongoing-SV-v2-2-shap', 'smm-ongoing-SV-v2-2-shap-sbd-v2')
        )
    ) AS OG 
    ON D.FBBID = OG.FBBID
    	AND D.EDATE = OG.SCORE_DT
),
CDC_OB AS (
  SELECT
    DISTINCT IDENTIFIER_ID AS FBBID,
    CASE
      WHEN SCORE <= 0.01 THEN 1
      WHEN SCORE <= 0.02 THEN 2
      WHEN SCORE <= 0.03 THEN 3
      WHEN SCORE <= 0.04 THEN 4
      WHEN SCORE <= 0.05 THEN 5
      WHEN SCORE <= 0.06 THEN 6
      WHEN SCORE <= 0.07 THEN 7
      WHEN SCORE <= 0.08 THEN 8
      WHEN SCORE <= 0.09 THEN 9
      WHEN SCORE <= 0.1 THEN 10
      WHEN SCORE <= 0.125 THEN 11
      WHEN SCORE <= 0.150 THEN 12
      WHEN SCORE <= 0.175 THEN 13
      WHEN SCORE <= 0.2 THEN 14
      WHEN SCORE <= 1 THEN 15
    END AS CURRENT_ONBOARDING
  FROM CDC_V2.SCORING.SCORING_MODEL_SCORES
  WHERE MODEL_NAME = 'smm-eaglet-SV-dac-v9-9-v3'
  QUALIFY ROW_NUMBER() OVER(PARTITION BY IDENTIFIER_ID ORDER BY PREDICTION_TIME DESC) = 1
)
SELECT
   DISTINCT A.LOAN_KEY,
    A.EDATE,
    DPD_BUCKET,
    DPD_DAYS,
    CHARGEOFF_PRINCIPAL,
    ORIGINAL_TERM,
    "Repurchase Date",
    CASE
    	WHEN ZEROIFNULL(IS_FRAUD) = 1 OR BK_FILING_DATE IS NOT NULL THEN 1
    	ELSE 0
    END BK_FRAUD,             
    FBX_PLUS,
    IS_CHARGED_OFF,
    CHARGE_OFF_DATE,
    RLD.FROM_SPV,
    CASE 
        WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) = 'BUYER_FINANCE_WEEKLY_FIXED' THEN CONCAT(ORIGINAL_TERM, ' Weeks Amortized') WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) LIKE '%TERM_LOAN_24%' THEN '24 Week TL' WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) LIKE '%TERM_LOAN_52%' THEN '52 Week TL' WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) LIKE '%QUAD_WEEKLY_12%' THEN 'Quad Weekly 12 Weeks' WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) LIKE '%QUAD_WEEKLY_24%' THEN 'Quad Weekly 24 Weeks' WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) LIKE '%QUAD_WEEKLY_52%' THEN 'Quad Weekly 52 Weeks' WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) LIKE '%AMORTIZED_12%' THEN '12 Weeks Amortized' WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) LIKE '%AMORTIZED_24%' THEN '24 Weeks Amortized' WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) LIKE '%AMORTIZED_52%' THEN '52 Weeks Amortized' WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) LIKE '%AMORTIZED_12%' THEN '12 Weeks Amortized' WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) LIKE '%BASED_WEEKLY%' THEN 'Custom Weekly' WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) ILIKE '%BASED%QUAD%WEEKLY%' THEN 'Custom Monthly' WHEN UPPER(ORIGINAL_PAYMENT_PLAN_NAME) ILIKE '%WEEKLY%FIXED%' THEN CONCAT(ORIGINAL_TERM, ' Weeks Amortized') WHEN ORIGINAL_PAYMENT_PLAN_NAME IS NULL THEN NULL ELSE ORIGINAL_PAYMENT_PLAN_NAME
    END ORIGINAL_PAYMENT_PLAN,
    CASE 
        WHEN ACTIVE_PAYMENT_PLAN_CONTEXT = 'PAY_EARLY' THEN 'Pay Early Plan' WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) = 'BUYER_FINANCE_WEEKLY_FIXED' THEN CONCAT(ACTIVE_PAYMENT_PLAN_DURATION, ' Weeks Amortized') WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) LIKE '%TERM_LOAN_24%' THEN '24 Week TL' WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) LIKE '%TERM_LOAN_52%' THEN '52 Week TL' WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) LIKE '%QUAD_WEEKLY_12%' THEN 'Quad Weekly 12 Weeks' WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) LIKE '%QUAD_WEEKLY_24%' THEN 'Quad Weekly 24 Weeks' WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) LIKE '%QUAD_WEEKLY_52%' THEN 'Quad Weekly 52 Weeks' WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) LIKE '%AMORTIZED_12%' THEN '12 Weeks Amortized' WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) LIKE '%AMORTIZED_24%' THEN '24 Weeks Amortized' WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) LIKE '%AMORTIZED_52%' THEN '52 Weeks Amortized' WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) LIKE '%AMORTIZED_12%' THEN '12 Weeks Amortized' WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) LIKE '%BASED_WEEKLY%' THEN 'Custom Weekly' WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) ILIKE '%BASED%QUAD%WEEKLY%' THEN 'Custom Monthly' WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) ILIKE '%WEEKLY%FIXED%' THEN CONCAT(ACTIVE_PAYMENT_PLAN_DURATION, ' Weeks Amortized') WHEN ACTIVE_PAYMENT_PLAN_NAME IS NULL THEN NULL ELSE ACTIVE_PAYMENT_PLAN_NAME 
    END ACTIVE_PAYMENT_PLAN,
    CASE 
        WHEN UPPER(ACTIVE_PAYMENT_PLAN_NAME) LIKE '%BASED%' THEN ACTIVE_PAYMENT_PLAN ELSE ORIGINAL_PAYMENT_PLAN
    END PAYMENT_PLAN,
     CASE
    	WHEN PAYMENT_PLAN IN  ('Quad Weekly 12 Weeks', 'Quad Weekly 24 Weeks','Quad Weekly 52 Weeks', 'Custom Monthly')  THEN 1
    	ELSE 0
    END QUAD_WEEKLY, 
    CASE
    	WHEN OWNERSHIP_DATE IS NOT NULL AND LOAN_CREDITED_DATE IS NOT NULL THEN 1
    	ELSE 0
    END FBX_OWNED,
    CASE
	    WHEN A.EDATE > '1/31/2025' THEN COALESCE(TO_SPV, SPV::STRING)
		WHEN (A.EDATE < CRB_REPURCHASES.REPURCHASE_WEEK OR  CRB_REPURCHASES.REPURCHASE_WEEK IS NULL) AND CRB_PLEDGED.TRANSFER_DATE <= A.EDATE THEN 'FBX_PT6'
		WHEN A.EDATE >= DATEADD(DAY, 1 - DAYOFWEEK(CS_PLEDGED.TRANSFER_DATE), CS_PLEDGED.TRANSFER_DATE) AND DATEADD(DAY, 1 - DAYOFWEEK(CS_PLEDGED.TRANSFER_DATE), CS_PLEDGED.TRANSFER_DATE) > DATEADD(DAY, 1 - DAYOFWEEK(CS_REPURCHASESV2.REPURCHASE_WEEK), CS_REPURCHASESV2.REPURCHASE_WEEK)  THEN 'FBX_PT5'
		--                    IF repurchased BY cs THEN fbx inc
		WHEN A.EDATE >= DATEADD(DAY, 1 - DAYOFWEEK(CS_REPURCHASESV2.REPURCHASE_WEEK), CS_REPURCHASESV2.REPURCHASE_WEEK) THEN 'FBX_INC'
		--                    IF repurchased BY crb and not transfered to cs THEN fbx inc
		WHEN A.EDATE >= DATEADD(DAY, 1 - DAYOFWEEK(CRB_REPURCHASES.REPURCHASE_WEEK), CRB_REPURCHASES.REPURCHASE_WEEK) AND A.EDATE < DATEADD(DAY, 1 - DAYOFWEEK(CS_PLEDGED.TRANSFER_DATE), CS_PLEDGED.TRANSFER_DATE) THEN 'FBX_INC'
		--                    IF pledged to crb and not transfered to cs then crb
		   -- WHEN A.EDATE >= DATEADD(DAY, 1 - DAYOFWEEK(CRB_PLEDGED.TRANSFER_DATE), CRB_PLEDGED.TRANSFER_DATE) AND A.EDATE < DATEADD(DAY, 1 - DAYOFWEEK(CS_PLEDGED.TRANSFER_DATE), CS_PLEDGED.TRANSFER_DATE) THEN 'FBX_PT6'
		--                    IF pledged to cs and not yet repurchased by cs then cs
		WHEN A.EDATE >= DATEADD(DAY, 1 - DAYOFWEEK(CS_PLEDGED.TRANSFER_DATE), CS_PLEDGED.TRANSFER_DATE) AND A.EDATE < DATEADD(DAY, 1 - DAYOFWEEK(CS_REPURCHASESV2.REPURCHASE_WEEK), CS_REPURCHASESV2.REPURCHASE_WEEK) THEN 'FBX_PT5'
		--                    IF pledged to cs, not repurchased by cs or transferred to crb then cs
		WHEN A.EDATE >= DATEADD(DAY, 1 - DAYOFWEEK(CS_PLEDGED.TRANSFER_DATE), CS_PLEDGED.TRANSFER_DATE) AND A.EDATE <= MAX(CASE WHEN SPV::STRING = 'FBX_PT5' THEN A.EDATE END) OVER(PARTITION BY A.LOAN_KEY) THEN 'FBX_PT5'
		--                    IF pledged to crb, not repurchased by crb or transferred to cs then crb
		  --  WHEN A.EDATE >= DATEADD(DAY, 1 - DAYOFWEEK(CRB_PLEDGED.TRANSFER_DATE), CRB_PLEDGED.TRANSFER_DATE) AND A.EDATE <= MAX(CASE WHEN SPV::STRING = 'FBX_PT6' THEN A.EDATE END) OVER(PARTITION BY A.LOAN_KEY) THEN 'FBX_PT6'
		--                    If Loan is pledged on cutoff day(or within 3 days greater) then CS
	   	WHEN (DATEDIFF(DAY, DATEADD(DAY, 1 - DAYOFWEEK(CS_PLEDGED.TRANSFER_DATE), CS_PLEDGED.TRANSFER_DATE), A.EDATE) BETWEEN 0 AND 3) AND DATEADD(DAY, 1 - DAYOFWEEK(CS_REPURCHASESV2.REPURCHASE_WEEK), CS_REPURCHASESV2.REPURCHASE_WEEK) IS NULL THEN 'FBX_PT5'
		--                    IF PIMCO then PIMCO
		WHEN SPV::STRING = 'FBX_PT4' THEN 'FBX_PT4'
		ELSE 'FBX_INC'
    END ACTUAL_SPV,
    CRB_REPURCHASES.REPURCHASE_WEEK CRB_REPURCHASED,
    CS_REPURCHASESV2.REPURCHASE_WEEK CS_REPURCHASED,
    CASE
	    WHEN A.EDATE > '1/31/2025' AND "Repurchase Date" <= A.EDATE THEN 1
    	WHEN A.EDATE <= '1/31/2025' AND CRB_REPURCHASES.REPURCHASE_WEEK <= A.EDATE or CS_REPURCHASESV2.REPURCHASE_WEEK <= A.EDATE THEN 1
    	WHEN A.EDATE <= '1/31/2025' AND ((CRB_REPURCHASED > A.EDATE OR CRB_REPURCHASED IS NULL) AND (CS_REPURCHASED > A.EDATE OR CS_REPURCHASED IS NULL)) THEN 0
    	ELSE 0
    END REPURCHASED,
    CASE
	    WHEN A.EDATE > '1/31/2025' AND "Repurchase Date" <= A.EDATE THEN 1
    	WHEN A.EDATE <= '1/31/2025' AND CRB_REPURCHASES.REPURCHASE_WEEK <= A.EDATE THEN 1
    	WHEN A.EDATE <= '1/31/2025' AND ((CRB_REPURCHASED > A.EDATE OR CRB_REPURCHASED IS NULL) AND (CS_REPURCHASED > A.EDATE OR CS_REPURCHASED IS NULL)) THEN 0
    	ELSE 0
    END "Repurchased CRB",
    CASE
	    WHEN A.EDATE > '1/31/2025' AND "Repurchase Date" <= A.EDATE THEN 1
    	WHEN A.EDATE <= '1/31/2025' AND CS_REPURCHASESV2.REPURCHASE_WEEK <= A.EDATE THEN 1
    	WHEN A.EDATE <= '1/31/2025' AND ((CRB_REPURCHASED > A.EDATE OR CRB_REPURCHASED IS NULL) AND (CS_REPURCHASED > A.EDATE OR CS_REPURCHASED IS NULL)) THEN 0
    	ELSE 0
    END "Repurchased ATLAS",
    CASE
    	WHEN PAYMENT_PLAN IN ('Quad Weekly 12 Weeks', 'Quad Weekly 24 Weeks','Quad Weekly 52 Weeks', 'Custom Monthly') AND zeroifnull(DPD_BUCKET) > 1 THEN 1
    	WHEN PAYMENT_PLAN NOT IN ('Quad Weekly 12 Weeks', 'Quad Weekly 24 Weeks','Quad Weekly 52 Weeks', 'Custom Monthly') AND zeroifnull(DPD_BUCKET) > 3 THEN 1
    	ELSE 0
    END MPFQUALIFICATION,
    CS_DEBT_DRAWN,
    CRB_DEBT_DRAWN,
    RESTRICTED_CASH_CRB,
    RESTRICTED_CASH_CS,
    OUTSTANDING_PRINCIPAL_FUNDED,
    CREDIT_LIMIT,
    CASE
    	WHEN UPPER(SUB_PRODUCT) LIKE '%LINE%' THEN 1
    	ELSE 0
    END LOC,
    BREATHER_PLAN_COUNT,
    IS_IN_BREATHER,
    CASE
        WHEN LOAN_OPERATIONAL_STATUS = 'FROZ' THEN 1 ELSE 0
    END WAIVER_COUNT,
    CASE
    	WHEN (CUSTOM_PLAN_COUNT+WAIVER_COUNT+(CASE WHEN ZEROIFNULL(BREATHER_PLAN_COUNT) > 2 THEN 1 ELSE 0 END)) > 1 THEN 1
    	ELSE 0
    END CS_MATERIAL_MODIFICATION,
    CASE
    	WHEN (CUSTOM_PLAN_COUNT+WAIVER_COUNT+ZEROIFNULL(BREATHER_PLAN_COUNT)) > 1 THEN 1
    	ELSE 0
    END CRB_MATERIAL_MODIFICATION,
    CASE
    	WHEN (QUAD_WEEKLY = 1 and zeroifnull(FBX_PLUS) = 0) THEN 1
    	ELSE 0
    END PAYMENT_FREQUENCY,
    CASE
        WHEN DATEDIFF('day', FIRST_APPROVED_DATE, LOAN_CREATED_DATE) >= 30 
        THEN OG_SCORE
    END OG_SCORING,
    CASE
        WHEN ORIGINAL_TERM = 52 AND DATEDIFF('day', FIRST_APPROVED_DATE, LOAN_CREATED_DATE) >= 30 THEN 0
        WHEN ORIGINAL_TERM = 52 AND DATEDIFF('day', FIRST_APPROVED_DATE, LOAN_CREATED_DATE) < 30 THEN 1
        ELSE 0
    END NEW_OR_EXISTING,
    coalesce(OB_TO_USE,CURRENT_ONBOARDING)  as Ob_score_to_use,
    COALESCE(CSR.Risk_score, june.risk_score, OG_SCORE, Ob_score_to_use) RISK_SCORE_TRUE,
    CASE
    	WHEN RISK_SCORE_TRUE >=13 THEN 1
    	ELSE 0
    END "Risk Score >=13",
    ZEROIFNULL(CASE
    	WHEN ORIGINAL_TERM = 52 THEN 1
    	ELSE 0
    END) "52W Allocation",
    ZEROIFNULL(CASE
    	WHEN ORIGINAL_TERM = 52 AND RISK_SCORE_TRUE BETWEEN 5 AND 8 THEN 1
    	ELSE 0
    END) "52W Risk Score",
    CASE
    	WHEN RISK_SCORE_TRUE >=13 AND A.ORIGINAL_TERM >= 24 THEN 1
    	ELSE 0
    END "Oterm >= 24 and Risk Score >=13" ,
    CASE
	    WHEN (CS_MATERIAL_MODIFICATION + PAYMENT_FREQUENCY + MPFQUALIFICATION + BK_FRAUD + 
	         (CASE WHEN (Original_Term = 52 AND ZEROIFNULL(FBX_PLUS) = 0) THEN 1 ELSE 0 END)
	         ) = 0
	    THEN 1
	    ELSE 0
	END AS CS_Eligible,
	CASE
		WHEN ORIGINAL_TERM = 52 AND ACQ_CHANNEL IN ('Organic', 'Direct') THEN 1
		ELSE 0
	END ACQ_CHANNEL_FIFTY_TWO,
	CASE
		WHEN ORIGINAL_TERM = 52 AND ZEROIFNULL(Ob_score_to_use) > 7 THEN 1
		WHEN ORIGINAL_TERM = 52 AND ZEROIFNULL(RISK_SCORE_TRUE) <= 8 THEN 0
		ELSE 0
	END RISK_SCORE_FIFTY_TWO,	
    CASE 
    	WHEN CRB_MATERIAL_MODIFICATION = 0 AND PAYMENT_FREQUENCY = 0 AND MPFQUALIFICATION = 0 AND BK_FRAUD = 0 AND ACQ_CHANNEL_FIFTY_TWO = 0 and CREDIT_LIMIT <= 150000 THEN 1
    	ELSE 0
    END CRB_Eligible,
    CASE 
    	WHEN CREDIT_LIMIT > 100000 THEN 1
    	ELSE 0
    END Credit_limit_100K,
    CASE 
    	WHEN CREDIT_LIMIT > 120000 THEN 1
    	ELSE 0
    END Credit_limit_120K,
    CUSTOM_PLAN_COUNT + WAIVER_COUNT + ZEROIFNULL(BREATHER_PLAN_COUNT) AS  "Total Modifications",
    ZEROIFNULL(GET(GET(PARSE_JSON(REGEXP_REPLACE(DACD.CREDIT_SCORE_JSON::VARCHAR , 'VantageScore [0-9].[0-9]', 'VantageScore')),'VantageScore'),'score')) AS "Vantage Score",
    previous_pledge_date AS "Previous Pledge Date"
FROM pledging_dates PD
LEFT JOIN BI.FINANCE.FINANCE_METRICS_DAILY A
		ON PD.EDATE = A.EDATE
LEFT JOIN ANALYTICS.CAPITAL_MARKETS.CS_PLEDGED
       	ON A.LOAN_KEY = CS_PLEDGED.LOAN_KEY
LEFT JOIN ANALYTICS.CAPITAL_MARKETS.CS_REPURCHASESV2
		ON A.LOAN_KEY = CS_REPURCHASESV2.LOAN_KEY
LEFT JOIN ANALYTICS.CAPITAL_MARKETS.CRB_PLEDGED
		ON A.LOAN_KEY = CRB_PLEDGED.LOAN_KEY
LEFT JOIN ANALYTICS.CAPITAL_MARKETS.CRB_REPURCHASES
		ON A.LOAN_KEY = CRB_REPURCHASES.LOAN_KEY
LEFT JOIN FBXPLUS
        ON A.FBBID = FBXPLUS.FBBID AND A.LOAN_CREATED_DATE = FBXPLUS.EDATE
LEFT JOIN MODIFICATIONS 
