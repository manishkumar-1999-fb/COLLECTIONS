                ZEROIFNULL(MODEL_DEVELOPMENT_FLAG) MODEL_DEVELOPMENT_FLAG,
                IS_CHARGED_OFF,
                SUM(DISTINCT CUSTOM_PLAN_COUNT+WAIVER_COUNT+(CASE WHEN BREATHER_PLAN_COUNT > 2 THEN 1 ELSE 0 END)) OVER(PARTITION BY A.LOAN_KEY) MATERIAL_MODIFICATION,
                OUTSTANDING_PRINCIPAL_FUNDED,
