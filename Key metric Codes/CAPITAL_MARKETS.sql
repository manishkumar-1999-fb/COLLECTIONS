		ON A.FBBID = OB.FBBID
     left join (select loan_key, risk_score, FBBID from ANALYTICS.CAPITAL_MARKETS.DATA_ROOM_LOAN_TAPE_JUNE2025) JUNE
            on June.loan_key = A.loan_key
