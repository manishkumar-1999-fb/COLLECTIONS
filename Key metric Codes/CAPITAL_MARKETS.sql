CREATE OR REPLACE TABLE INDUS.PUBLIC.CAP_MARKET_23 AS
SELECT
	NEW_TRANSFER_DATE,
    CASE 
        WHEN DAYNAME(NEW_TRANSFER_DATE) = 'Wednesday' THEN NEW_TRANSFER_DATE  
        ELSE NEXT_DAY(NEW_TRANSFER_DATE, 'WEDNESDAY') 
    END AS week_end_date,
	ACTUAL_SPV SPV,
	VINTAGE,
	NEW_OR_EXISTING,
	FBX_PLUS,
	CASE
		WHEN RISK_SCORE_TRUE BETWEEN 1 AND 5 THEN '1 TO 5'
		WHEN RISK_SCORE_TRUE BETWEEN 6 AND 8 THEN '6 TO 8'
		WHEN RISK_SCORE_TRUE BETWEEN 9 AND 11 THEN '9 TO 11'
		WHEN RISK_SCORE_TRUE BETWEEN 13 AND 15 THEN '13 TO 15'
	END RISK_BUCKET,
	ORIGINAL_TERM,
	ACQ_CHANNEL,
	IS_MODEL_DEVELOPMENT,
	SUM(OS) NEW_TRANSFERED_ASSETS
FROM 
	(
	SELECT
		DISTINCT A.LOAN_KEY,
		SETPOINT_SPV.EDATE NEW_TRANSFER_DATE,
	    CASE
		    WHEN A.EDATE > '1/31/2025' THEN TO_SPV
			WHEN SPV::STRING = 'FBX_PT4' THEN 'FBX_PT4'
			ELSE 'FBX_INC'
	    END ACTUAL_SPV,
	    CASE
	    	WHEN UPPER(DACD.FUNDBOX_PLUS_STATUS) LIKE '%SUBSCRIBED%' THEN 1
	    	ELSE 0
	    END FBX_PLUS,	    
		CASE
			WHEN IS_CHARGED_OFF = 0 THEN OUTSTANDING_PRINCIPAL_FUNDED
		END OS,
		LAST_DAY(LOAN_CREATED_DATE) VINTAGE,
		ORIGINAL_TERM,
        coalesce(OBS.OB_TO_USE,CURRENT_ONBOARDING  ) as OB_USED,
		COALESCE(CSR.risk_score, JUNE.risk_Score , OG_SCORE, OB_USED) RISK_SCORE_TRUE,
	    SUB_PRODUCT,
	    CASE
	    	WHEN LAST_DAY(A.LOAN_CREATED_DATE, 'week') = LAST_DAY(FIRST_APPROVED_TIME::DATE, 'week') THEN 'New Customer' 
	    	ELSE 'Existing Customer'
	    END NEW_OR_EXISTING,    
		CASE
			WHEN LOWER(REGEXP_REPLACE(TRIM(CD.REGISTRATION_CAMPAIGN_ID), '\s+\d+$', '')) = LOWER(REGEXP_REPLACE(TRIM(TB.TERMINATED_BROKERS), '\s+\d+$', '')) THEN 'Brokers'
			WHEN PN.PARTNER_NAME = 'Intuit' THEN 'Intuit'
			WHEN LOWER(CD.REGISTRATION_CAMPAIGN_ID) IN ('intuit', 'intuit_appcenter', 'quickbooks-financing', 'intuit_campaigns') OR LOWER(REGISTRATION_CAMPAIGN_ID) LIKE '%intuit%' THEN 'Intuit'
			WHEN PN.PARTNER_NAME = 'Nav' THEN 'NAV'
			WHEN PN.PARTNER_NAME IN ('Lendingtree', 'Lendio') THEN 'Marketplace'
			WHEN LOWER(REGEXP_REPLACE(TRIM(CD.PARTNER_ATTRIBUTION), '\s+\d{4}$', '')) = LOWER(REGEXP_REPLACE(TRIM(PN.PARTNER_ATTRIBUTION), '\s+\d{4}$', '')) THEN 'Strategic Partners'
			WHEN LOWER(REGEXP_REPLACE(TRIM(CD.PARTNER_ATTRIBUTION), '\s+\d{4}$', '')) = LOWER(REGEXP_REPLACE(TRIM(PN.PARTNER_NAME), '\s+\d{4}$', '')) THEN 'Strategic Partners'
			WHEN LOWER(CD.PARTNER_ATTRIBUTION) IN ('organic','content', 'fundboxpay', 'content 100817571549') THEN 'Organic'
	        WHEN LOWER(CD.PARTNER_ATTRIBUTION) LIKE '%direct%' THEN 'Direct'
			WHEN (PN.PARTNER_NAME IS NULL AND CD.PARTNER_ATTRIBUTION IN ('non-partner')) THEN 'Organic'
			WHEN (PN.PARTNER_NAME IS NULL) THEN 'Terminated Channel'
			ELSE 'Brokers'
		END ACQ_CHANNEL,
	    CASE
	    	WHEN (IS_SAVED_BY_CONTROL_GROUP+IS_SAVED_BY_PARTNERSHIP) > 0 THEN 1
	    	ELSE 0
	    END IS_MODEL_DEVELOPMENT
	FROM ANALYTICS.CAPITAL_MARKETS.PLEDGE_DATES
	LEFT JOIN BI.FINANCE.FINANCE_METRICS_DAILY A
		ON A.EDATE = PLEDGE_DATES.EDATE
	LEFT JOIN (SELECT FBBID, EDATE, FUNDBOX_PLUS_STATUS, SUB_PRODUCT, RISK_CREDIT_REVIEW_MODEL_NAME, RISK_CREDIT_REVIEW_ALIGNED_BUCKET, YEAR_STARTED_EDATE, STATE,
	        CREDIT_SCORE_JSON, INDUSTRY_NAICS_CODE_EDATE, CREDIT_LIMIT, IS_SAVED_BY_CONTROL_GROUP, FIRST_APPROVED_TIME, IS_SAVED_BY_PARTNERSHIP FROM BI.PUBLIC.DAILY_APPROVED_CUSTOMERS_DATA) DACD
	        ON A.FBBID = DACD.FBBID AND A.LOAN_CREATED_DATE = DACD.EDATE
	LEFT JOIN SETPOINT_APPROVED_REPORT_DATA.RESULTS.REPORT_APPROVAL_RESULTS SETPOINT_SPV
		ON A.LOAN_KEY = SETPOINT_SPV.LOAN_KEY AND A.EDATE = SETPOINT_SPV.EDATE
	LEFT JOIN 
			(
			  SELECT
			    D.FBBID,
			    D.EDATE,
			    LAST_VALUE(CASE WHEN D.RISK_CREDIT_REVIEW_MODEL_NAME IN ('UnifiedOngoingMLEModelV2.2', 'sm-internalmodel_unified_og_v2.2_shap', 'smm-ongoing-SV-v2-2-shap', 'smm-ongoing-SV-v2-2-shap-sbd-v2') 
			    	THEN D.RISK_CREDIT_REVIEW_ALIGNED_BUCKET ELSE OG.RETRO_SCORE END) OVER(PARTITION BY D.FBBID, D.EDATE ORDER BY D.EDATE) AS OG_SCORE
			  FROM BI.PUBLIC.DAILY_APPROVED_CUSTOMERS_DATA AS d
			    LEFT JOIN (
			      SELECT
			        FBBID,
			        SCORE_DT,
			        FIRST_VALUE(UNI_BKT) IGNORE NULLS OVER(PARTITION BY FBBID ORDER BY SCORE_DT) AS RETRO_SCORE,
			        FIRST_VALUE(OG_SCORE) IGNORE NULLS OVER(PARTITION BY FBBID ORDER BY SCORE_DT) AS SCORE_SPECIFIC
			      FROM
			        (
			          SELECT
			            CAST(PREDICTION_TIME AS DATE) AS SCORE_DT,
			            IDENTIFIER_ID AS FBBID,
			            SCORE AS OG_SCORE,
			            CASE
			              WHEN SCORE <= 0.016 THEN 1
			              WHEN SCORE <= 0.019 THEN 2
			              WHEN SCORE <= 0.025 THEN 3
			              WHEN SCORE <= 0.03 THEN 4
			              WHEN SCORE <= 0.036 THEN 5
			              WHEN SCORE <= 0.043 THEN 6
			              WHEN SCORE <= 0.052 THEN 7
			              WHEN SCORE <= 0.063 THEN 8
			              WHEN SCORE <= 0.08 THEN 9
			              WHEN SCORE <= 0.11 THEN 10
			              WHEN SCORE <= 0.122 THEN 11
			              WHEN SCORE <= 0.135 THEN 12
			              WHEN SCORE <= 0.156 THEN 13
			              WHEN SCORE <= 0.195 THEN 14
			              WHEN SCORE <= 1 THEN 15
			            END AS UNI_BKT
			          FROM CDC_V2.SCORING.SCORING_MODEL_SCORES
			          WHERE MODEL_NAME IN ('UnifiedOngoingMLEModelV2.2', 'sm-internalmodel_unified_og_v2.2_shap', 'smm-ongoing-SV-v2-2-shap', 'smm-ongoing-SV-v2-2-shap-sbd-v2')
			        )
			    ) AS OG 
			    ON D.FBBID = OG.FBBID
			    	AND D.EDATE = OG.SCORE_DT
			) OG_MODEL
			ON A.FBBID = OG_MODEL.FBBID AND A.LOAN_CREATED_DATE = OG_MODEL.EDATE
	LEFT JOIN 
		(
		  SELECT
		    DISTINCT IDENTIFIER_ID AS FBBID,
		    CASE
		      WHEN SCORE <= 0.01 THEN 1
		      WHEN SCORE <= 0.02 THEN 2
		      WHEN SCORE <= 0.03 THEN 3
		      WHEN SCORE <= 0.04 THEN 4
		      WHEN SCORE <= 0.05 THEN 5
		      WHEN SCORE <= 0.06 THEN 6
		      WHEN SCORE <= 0.07 THEN 7
		      WHEN SCORE <= 0.08 THEN 8
		      WHEN SCORE <= 0.09 THEN 9
		      WHEN SCORE <= 0.1 THEN 10
		      WHEN SCORE <= 0.125 THEN 11
		      WHEN SCORE <= 0.150 THEN 12
		      WHEN SCORE <= 0.175 THEN 13
		      WHEN SCORE <= 0.2 THEN 14
		      WHEN SCORE <= 1 THEN 15
		    END AS CURRENT_ONBOARDING
		  FROM CDC_V2.SCORING.SCORING_MODEL_SCORES
		  WHERE MODEL_NAME = 'smm-eaglet-SV-dac-v9-9-v3'
		  QUALIFY ROW_NUMBER() OVER(PARTITION BY IDENTIFIER_ID ORDER BY PREDICTION_TIME DESC) = 1
