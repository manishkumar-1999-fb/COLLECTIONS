            		ON A.FBBID = BK_FRAUD.FBBID AND A.LOAN_CREATED_DATE = BK_FRAUD.EDATE
            LEFT JOIN (SELECT FBBID, EDATE, SUB_PRODUCT FROM BI.PUBLIC.DAILY_APPROVED_CUSTOMERS_DATA) DACD
