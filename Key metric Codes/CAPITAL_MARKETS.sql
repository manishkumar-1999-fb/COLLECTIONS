            on CSR.loan_key = A.loan_key
    left join ANALYTICS.CAPITAL_MARKETS.OB_SCORES OBS
                on OBS.loan_key = A.loan_key
	LEFT JOIN (SELECT FBBID, PARTNER_ATTRIBUTION, REGISTRATION_CAMPAIGN_ID FROM BI."PUBLIC".CUSTOMERS_DATA) CD
