--Balance Sheet / Total Metric
CREATE OR REPLACE TABLE INDUS.PUBLIC.BALANCE_SHEET_EVAL_RG AS 
WITH pledging_dates as (
select edate, lag(edate) over(order by edate asc) as previous_pledge_date from ANALYTICS.CAPITAL_MARKETS.PLEDGE_DATES 
),
BREATHERS AS (
	SELECT
	    A.FBBID,
	    A.LOAN_KEY,
	    A.EDATE,
	    FROM_DATE BREATHER_START_DATE,
	    TO_DATE BREATHER_END_DATE,
	    (TO_DATE - FROM_DATE) BREATHER_LENGTH,
	    CASE
	        WHEN A.EDATE BETWEEN BREATHER_START_DATE AND BREATHER_END_DATE THEN 1
	        ELSE 0
	    END IS_IN_BREATHER,
	    CASE
	        WHEN A.EDATE BETWEEN BREATHER_START_DATE AND BREATHER_END_DATE THEN BREATHER_LENGTH
	        ELSE 0
	    END BREATHER_DURATION,
	        ROUND(((SUM(CASE WHEN BREATHER_START_DATE IS NOT NULL THEN BREATHER_LENGTH END) OVER(PARTITION BY LOAN_KEY))/7),0) BREATHER_PLAN_COUNT
	FROM BI.FINANCE.FINANCE_METRICS_DAILY A
	LEFT JOIN BI.FINANCE.DIM_BREATHER_EVENT B
	        ON A.FBBID = B.FBBID
	                AND A.EDATE>=B.FROM_DATE
	                AND A.EDATE<=B.TO_DATE
	WHERE FROM_DATE IS NOT NULL
		AND YEAR(EDATE) > 2023
	GROUP BY 1,2,3,4,5,6,7
),
BREATHER_FINAL AS 
(
	SELECT
	        LOAN_KEY,
	        EDATE,
	        IS_IN_BREATHER,
	        BREATHER_DURATION,
	        zeroifnull(BREATHER_PLAN_COUNT) BREATHER_PLAN_COUNT,
	        BREATHER_START_DATE,
	        BREATHER_END_DATE
	FROM BREATHERS
	WHERE EDATE>=BREATHER_START_DATE 
	    AND EDATE<=BREATHER_END_DATE
),
ACQ_CHANNEL_CALC AS
(
	SELECT
		A.FBBID,
		A.LOAN_KEY,
		CASE
			WHEN LOWER(REGEXP_REPLACE(TRIM(CD.REGISTRATION_CAMPAIGN_ID), '\s+\d+$', '')) = LOWER(REGEXP_REPLACE(TRIM(TB.TERMINATED_BROKERS), '\s+\d+$', '')) THEN 'Brokers'
			WHEN PN.PARTNER_NAME = 'Intuit' THEN 'Intuit'
			WHEN LOWER(CD.REGISTRATION_CAMPAIGN_ID) IN ('intuit', 'intuit_appcenter', 'quickbooks-financing', 'intuit_campaigns') OR LOWER(REGISTRATION_CAMPAIGN_ID) LIKE '%intuit%' THEN 'Intuit'
			WHEN PN.PARTNER_NAME = 'Nav' THEN 'NAV'
			WHEN PN.PARTNER_NAME IN ('Lendingtree', 'Lendio') THEN 'Marketplace'
			WHEN LOWER(REGEXP_REPLACE(TRIM(CD.PARTNER_ATTRIBUTION), '\s+\d{4}$', '')) = LOWER(REGEXP_REPLACE(TRIM(PN.PARTNER_ATTRIBUTION), '\s+\d{4}$', '')) THEN 'Strategic Partners'
			WHEN LOWER(REGEXP_REPLACE(TRIM(CD.PARTNER_ATTRIBUTION), '\s+\d{4}$', '')) = LOWER(REGEXP_REPLACE(TRIM(PN.PARTNER_NAME), '\s+\d{4}$', '')) THEN 'Strategic Partners'
			WHEN LOWER(CD.PARTNER_ATTRIBUTION) IN ('organic','content', 'fundboxpay', 'content 100817571549') THEN 'Organic'
	        WHEN LOWER(CD.PARTNER_ATTRIBUTION) LIKE '%direct%' THEN 'Direct'
			WHEN (PN.PARTNER_NAME IS NULL AND CD.PARTNER_ATTRIBUTION IN ('non-partner')) THEN 'Organic'
			WHEN (PN.PARTNER_NAME IS NULL) THEN 'Terminated Channel'
